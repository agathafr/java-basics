
public class TestVariables {
	
	// Starting point of application 
	public static void main(String[] args) {
		System.out.println("Hello, this is a new test!");
		
		// Java is statically and strongly typed
		// You need to declare all the variables that will be used and
		// You need to declare the type
		
		// declaration
		int age;
		
		//assignment
		age = 37;
		
		System.out.println(age);
		
		age = 30 + 10;
		
		System.out.println(age);
		
		age = 3 * 5 + 2;
		
		System.out.println(age);
		
		System.out.println("Age is " + age);
	}
}
